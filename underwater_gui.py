    def _compute_results(self, events):
        stats = {"n_packets": len(events), "by_channel": defaultdict(int)}
        latencies = []
        energy = 0.0
        for e in events:
            if e.send_time and e.recv_time:
                lat = e.recv_time - e.gen_time
                latencies.append(lat)
            if e.channel == "ACOUSTIC":
                stats["by_channel"]["ACOUSTIC"] += 1
                energy += ENERGY_ACOUSTIC
            elif e.channel == "OPTICAL":
                stats["by_channel"]["OPTICAL"] += 1
                energy += ENERGY_OPTICAL
        stats["avg_latency"] = (sum(latencies)/len(latencies)) if latencies else 0.0
        stats["latency_list"] = latencies
        stats["energy"] = energy
        stats["pdr"] = (len(latencies) / max(1, stats["n_packets"])) if stats["n_packets"]>0 else 0.0
        stats["throughput"] = stats["n_packets"] / SIM_DURATION
        return stats

