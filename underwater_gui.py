    def load_log(self):
        path = filedialog.askopenfilename(title="Open JSON log", filetypes=[("JSON files","*.json"),("All files","*.*")])
        if not path: return
        try:
            import json
            with open(path,"r") as f:
                data = json.load(f)
            events = []
            for item in data.get("events", []):
                pkt = PacketEvent(item.get("id",0), item.get("src",1), item.get("dst",0), item.get("type","LOW"), item.get("gen",0.0))
                pkt.send_time = item.get("send", pkt.gen_time)
                pkt.recv_time = item.get("recv", pkt.send_time + 1.0)
                pkt.channel = item.get("channel", "ACOUSTIC")
                # preserve batch_id if provided
                if "batch_id" in item:
                    pkt.batch_id = item["batch_id"]
                events.append(pkt)
            name = os.path.splitext(os.path.basename(path))[0]
            self.scenario_results[name] = self._compute_results(events)
            self.expl_text.delete("1.0", tk.END); self.expl_text.insert(tk.END, f"Loaded log: {path}\\nEvents: {len(events)}\\nReplaying...")
            self._start_animation_with_events(events, scenario=name)
        except Exception as e:
            messagebox.showerror("Error", f"Failed to load log: {e}")

