class PacketEvent:
    def __init__(self, pkt_id, src, dst, pkt_type, gen_time, size=1):
        self.pkt_id = pkt_id
        self.src = src
        self.dst = dst
        self.pkt_type = pkt_type
        self.gen_time = gen_time
        self.send_time = None
        self.recv_time = None
        self.channel = None

class BaseSimulator:
    def __init__(self, num_nodes=NUM_NODES, duration=SIM_DURATION):
        self.num_nodes = num_nodes
        self.duration = duration
        self.time = 0.0
        self.pkt_counter = 0

    def generate_stream(self):
        raise NotImplementedError

class AcousticSimulator(BaseSimulator):
    def generate_stream(self):
        while self.time < self.duration:
            src = random.randint(1, self.num_nodes-1)
            dst = 0
            pkt_type = "LOW"
            tgap = random.expovariate(PACKET_GEN_RATE)
            self.time += tgap
            self.pkt_counter += 1
            pkt = PacketEvent(self.pkt_counter, src, dst, pkt_type, self.time)
            propagation = 2.0 + random.uniform(0.0, 1.0)
            pkt.send_time = self.time + 0.01
            pkt.recv_time = pkt.send_time + propagation
            pkt.channel = "ACOUSTIC"
            yield pkt

class HybridSimulator(BaseSimulator):
    def generate_stream(self):
        while self.time < self.duration:
            src = random.randint(1, self.num_nodes-1)
            dst = 0
            pkt_type = "HIGH" if random.random() < 0.25 else "LOW"
            tgap = random.expovariate(PACKET_GEN_RATE)
            self.time += tgap
            self.pkt_counter += 1
            pkt = PacketEvent(self.pkt_counter, src, dst, pkt_type, self.time)
            pkt.send_time = self.time + 0.003
            if pkt_type == "HIGH":
                if random.random() < 0.06:
                    pkt.channel = "ACOUSTIC"
                    pkt.recv_time = pkt.send_time + (1.8 + random.random()*0.9)
                else:
                    pkt.channel = "OPTICAL"
                    pkt.recv_time = pkt.send_time + (0.18 + random.random()*0.35)
            else:
                pkt.channel = "ACOUSTIC"
                pkt.recv_time = pkt.send_time + (1.8 + random.random()*1.0)
            yield pkt

class TDMASimulator(HybridSimulator):
    def __init__(self, *a, slot_time=0.8, **kw):
        super().__init__(*a, **kw)
        self.slot_time = slot_time

    def generate_stream(self):
        slot = 1
        while self.time < self.duration:
            owner = ((slot-1) % (self.num_nodes-1)) + 1
            num_pkts = random.choice([0,1,1,2])
            for _ in range(num_pkts):
                src = owner; dst = 0
                pkt_type = "HIGH" if random.random() < 0.35 else "LOW"
                self.pkt_counter += 1
                t_offset = random.random() * self.slot_time
                self.time += t_offset
                pkt = PacketEvent(self.pkt_counter, src, dst, pkt_type, self.time)
                pkt.send_time = self.time + 0.002
                if pkt_type == "HIGH":
                    if random.random() < 0.05:
                        pkt.channel = "ACOUSTIC"
                        pkt.recv_time = pkt.send_time + (1.8 + random.random()*0.8)
                    else:
                        pkt.channel = "OPTICAL"
                        pkt.recv_time = pkt.send_time + (0.18 + random.random()*0.35)
                else:
                    pkt.channel = "ACOUSTIC"
                    pkt.recv_time = pkt.send_time + (1.6 + random.random()*1.0)
                yield pkt
            self.time += max(0.0, self.slot_time*0.5)
            slot += 1

class AggregationSimulator(HybridSimulator):
    def __init__(self, *a, buffer_size=5, buffer_timeout=2.0, **kw):
        super().__init__(*a, **kw)
        self.buffer_size = buffer_size
        self.buffer_timeout = buffer_timeout

    def generate_stream(self):
        buffer = []
        buffer_start = None
        while self.time < self.duration:
            src = random.randint(1, self.num_nodes-1)
            dst = 0
            pkt_type = "HIGH" if random.random() < 0.22 else "LOW"
            tgap = random.expovariate(PACKET_GEN_RATE)
            self.time += tgap
            self.pkt_counter += 1
            pkt = PacketEvent(self.pkt_counter, src, dst, pkt_type, self.time)
            buffer.append(pkt)
            if buffer_start is None:
                buffer_start = self.time
            if len(buffer) >= self.buffer_size or (self.time - buffer_start) >= self.buffer_timeout:
                send_base = self.time + 0.02
                # record burst info in a simple structure returned via packet event attribute 'batch_id'
                batch_id = int(self.time*1000)
                for i,bp in enumerate(buffer):
                    bp.send_time = send_base + 0.001*i
                    bp.batch_id = batch_id
                    if bp.pkt_type == "HIGH":
                        if random.random() < 0.04:
                            bp.channel = "ACOUSTIC"
                            bp.recv_time = bp.send_time + (1.8 + random.random()*0.8)
                        else:
                            bp.channel = "OPTICAL"
                            bp.recv_time = bp.send_time + (0.18 + random.random()*0.35)
                    else:
                        bp.channel = "ACOUSTIC"
                        bp.recv_time = bp.send_time + (1.6 + random.random()*1.0)
                    yield bp
                buffer = []; buffer_start = None
        for i,bp in enumerate(buffer):
            bp.send_time = self.time + 0.01 + 0.001*i
            bp.batch_id = int(self.time*1000)
            if bp.pkt_type == "HIGH":
                bp.channel = "OPTICAL"
                bp.recv_time = bp.send_time + (0.2 + random.random()*0.3)
            else:
                bp.channel = "ACOUSTIC"
                bp.recv_time = bp.send_time + (1.8 + random.random()*1.0)
            yield bp

