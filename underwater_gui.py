            src = self.node_positions[e.src]; dst = self.node_positions[e.dst]
            lifetime = max(0.2, (e.recv_time - e.send_time))
            color = COLOR_OPTICAL if e.channel == "OPTICAL" else COLOR_ACOUSTIC
            marker = "D" if e.pkt_type=="HIGH" else "o"
            p = {"event": e, "src": src, "dst": dst, "t0": sim_elapsed, "lifetime": lifetime, "color": color, "marker": marker}
